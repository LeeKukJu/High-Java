package ment.vo;

public class MentVO {
	private int mentNo;			//댓글번로
	private int boNo;			//게시글번호
	private String cateCode;	//게시글 카테고리코드
	private String memId;		//회원 아이디
	private String memNick;		//회원닉네임
	private String mentContents;//댓글내용
	private String mentCDate;	//댓글작성일
	private String mentMDate;	//댓글수정일
	private String mentParent;	//부모댓글 -대댓글 사용할 경우 사용		
	private String boTitle; // 게시글 제목
	private String cateName; // 카테고리 명

	public String getCateName() {
		return cateName;
	}

	public void setCateName(String cateName) {
		this.cateName = cateName;
	}

	public int getMentNo() {
		return mentNo;
	}

	public String getBoTitle() {
		return boTitle;
	}

	public void setBoTitle(String boTitle) {
		this.boTitle = boTitle;
	}

	public void setMentNo(int mentNo) {
		this.mentNo = mentNo;
	}

	public int getBoNo() {
		return boNo;
	}

	public void setBoNo(int boNo) {
		this.boNo = boNo;
	}

	public String getCateCode() {
		return cateCode;
	}

	public void setCateCode(String cateCode) {
		this.cateCode = cateCode;
	}

	public String getMemId() {
		return memId;
	}

	public void setMemId(String memId) {
		this.memId = memId;
	}

	public String getMentContents() {
		return mentContents;
	}

	public void setMentContents(String mentContents) {
		this.mentContents = mentContents;
	}

	public String getMentCDate() {
		return mentCDate;
	}

	public void setMentCDate(String mentCDate) {
		this.mentCDate = mentCDate;
	}

	public String getMentMDate() {
		return mentMDate;
	}

	public void setMentMDate(String mentMDate) {
		this.mentMDate = mentMDate;
	}

	public String getMentParent() {
		return mentParent;
	}

	public void setMentParent(String mentParent) {
		this.mentParent = mentParent;
	}


	public String getMemNick() {
		return memNick;
	}

	public void setMemNick(String memNick) {
		this.memNick = memNick;
	}
	
	@Override
	public String toString() {
		return "MentVO [mentNo=" + mentNo + ", boNo=" + boNo + ", cateCode=" + cateCode + ", memId=" + memId
				+ ", mentContents=" + mentContents + ", mentCDate=" + mentCDate + ", mentMDate=" + mentMDate
				+ ", mentParent=" + mentParent + "]";
	}
	
}
