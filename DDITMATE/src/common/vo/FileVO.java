package common.vo;

public class FileVO {

	private String fileSavePath;
	private String fileSaveName;
	private String fileOrignName;
	private String fileExtsn;
	private String fileUrl;
	
	public String getFileUrl() {
		return fileUrl;
	}
	public void setFileUrl(String fileUrl) {
		this.fileUrl = fileUrl;
	}
	public String getFileSavePath() {
		return fileSavePath;
	}
	public void setFileSavePath(String fileSavePath) {
		this.fileSavePath = fileSavePath;
	}
	public String getFileSaveName() {
		return fileSaveName;
	}
	public void setFileSaveName(String fileSaveName) {
		this.fileSaveName = fileSaveName;
	}
	public String getFileOrignName() {
		return fileOrignName;
	}
	public void setFileOrignName(String fileOrignName) {
		this.fileOrignName = fileOrignName;
	}
	public String getFileExtsn() {
		return fileExtsn;
	}
	public void setFileExtsn(String fileExtsn) {
		this.fileExtsn = fileExtsn;
	}
	@Override
	public String toString() {
		return "FileVO [fileSavePath=" + fileSavePath + ", fileSaveName=" + fileSaveName + ", fileOrignName="
				+ fileOrignName + ", fileUrl=" + fileUrl + "]";
	}
	
	
	
	
}
